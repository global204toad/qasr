// Function to translate product descriptions based on product name
export const getDescriptionTranslation = (description, language = 'ar', productName = '') => {
    if (!description || language === 'en') return description;

    // If we have a product name, try to find its description key
    if (productName) {
        const descKey = productNameToDescKey[productName] || productNameToDescKey[productName.trim()];
        if (descKey) {
            const translated = translations[language]?.[descKey];
            if (translated) return translated;
        }
    }

    // Return original if no translation found
    return description;
};
