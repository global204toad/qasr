// Function to translate product descriptions based on product name
export const getDescriptionTranslation = (description, language = 'ar', productName = '') => {
  // If no description or language is English, return original
  if (!description || language === 'en') return description;

  // If we have a product name, try to find its description key
  if (productName) {
    const trimmedName = productName.trim();

    // Try exact match first
    let descKey = productNameToDescKey[trimmedName];

    // If no exact match, try case-insensitive match for English names
    if (!descKey) {
      const lowerName = trimmedName.toLowerCase();
      const foundKey = Object.keys(productNameToDescKey).find(
        key => key.toLowerCase() === lowerName
      );
      if (foundKey) {
        descKey = productNameToDescKey[foundKey];
      }
    }

    // If we found a description key, look it up in translations
    if (descKey) {
      const translated = translations[language]?.[descKey];
      if (translated) {
        return translated;
      }
    }
  }

  // Return original description if no translation found
  return description;
};
